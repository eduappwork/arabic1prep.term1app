const rotate = <T,>(items: T[], amount: number) =>
  items.map((_, index) => items[(index + amount) % items.length]);

export function buildLessonQuestions(target: Lesson): Question[] {
  const multipleChoice = target.facts.map((item, index) => ({
    id: `${target.id}-mcq-${index + 1}`,
    lessonId: target.id,
    type: "اختيار من متعدد" as const,
    prompt: item.question,
    options: rotate([item.answer, ...item.wrong], index % 4),
    answer: item.answer,
    explanation: item.explanation,
  }));

  const trueFalse = target.facts.map((item, index) => {
    const isTrue = index % 2 === 0;
    const claimed = isTrue ? item.answer : item.wrong[0];
    return {
      id: `${target.id}-tf-${index + 1}`,
      lessonId: target.id,
      type: "صواب أم خطأ" as const,
      prompt: `الإجابة عن «${item.question}» هي «${claimed}».`,
      options: ["صواب", "خطأ"],
      answer: isTrue ? "صواب" : "خطأ",
      explanation: item.explanation,
    };
  });

  const completion = target.facts.map((item, index) => ({
    id: `${target.id}-fill-${index + 1}`,
    lessonId: target.id,
    type: "أكمل" as const,
    prompt: `أكمل باختيار الإجابة الصحيحة: ${item.question}`,
    options: rotate([item.answer, ...item.wrong], (index + 2) % 4),
    answer: item.answer,
    explanation: item.explanation,
  }));

  return [...multipleChoice, ...trueFalse, ...completion];
}

export function buildUnitBank(target: Unit): Question[] {
  const pools = target.lessons.map(buildLessonQuestions);
  const result: Question[] = [];
  let row = 0;
  while (result.length < 50) {
    for (const pool of pools) {
      if (result.length === 50) break;
      const source = pool[row % pool.length];
      result.push({ ...source, id: `${target.id}-bank-${result.length + 1}` });
    }
    row += 1;
  }
  return result;
}

export const allLessons = units.flatMap((unit) => unit.lessons);

export function getLesson(lessonId: string) {
  return allLessons.find((item) => item.id === lessonId);
}

export function getUnit(unitId: string) {
  return units.find((item) => item.id === unitId);
}
